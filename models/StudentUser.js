const mongoose = require('mongoose');

const StudentUserSchema = mongoose.Schema({
    // PROPERTIES OF A STUDENT USER
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('StudentUser', StudentUserSchema);